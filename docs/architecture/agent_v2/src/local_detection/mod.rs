/// Bloom filter for fast IOC matching.
///
/// Uses a two-level approach:
/// 1. Bloom filter for O(1) negative lookup (99.9% of events)
/// 2. HashSet for confirmation on positive bloom hits
pub struct IocBloomFilter {
    // In production: probabilistic::BloomFilter + HashSet<String>
    pub bad_hashes: std::collections::HashSet<String>,
    pub bad_domains: std::collections::HashSet<String>,
    pub bad_ips: std::collections::HashSet<String>,
}

impl std::fmt::Debug for IocBloomFilter {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "IocBloomFilter(hashes={}, domains={}, ips={})",
            self.bad_hashes.len(),
            self.bad_domains.len(),
            self.bad_ips.len()
        )
    }
}

