//! SentinelAI Agent v2 — Local Detection Module
//!
//! On-agent detection engine that evaluates telemetry events against
//! signature rules, YARA patterns, and heuristics before they leave
//! the endpoint.
//!
//! # Why Local Detection?
//!
//! 1. **Latency**: Critical detections (Mimikatz, ransomware) trigger
//!    local response actions within milliseconds, not seconds.
//! 2. **Resilience**: Detection continues even if backend is unreachable.
//! 3. **Bandwidth**: Only high-confidence alerts need enrichment server-side.
//! 4. **Defense-in-depth**: Even if network detection is bypassed, local
//!    detection provides a second chance.
//!
//! # Architecture
//!
//! ```text
//! NormalizedEvent (from pipeline)
//!      │
//!      ▼
//! ┌──────────────────────────────────────────────────────┐
//! │              Local Detection Engine                    │
//! │                                                        │
//! │  ┌──────────────┐  ┌────────────┐  ┌──────────────┐  │
//! │  │ Sigma Rules   │  │ YARA Rules  │  │ Heuristics   │  │
//! │  │ (500+ rules)  │  │ (file scan) │  │ (built-in)   │  │
//! │  └──────┬───────┘  └──────┬─────┘  └──────┬───────┘  │
//! │         │                 │                │          │
//! │         └────────┬────────┘────────────────┘          │
//! │                  │                                     │
//! │                  ▼                                     │
//! │         ┌─────────────────┐                            │
//! │         │  Alert Manager   │                            │
//! │         │  (dedup, queue)  │                            │
//! │         └────────┬────────┘                            │
//! │                  │                                     │
//! │                  ▼                                     │
//! │  ┌─────────────────────────────────────┐              │
//! │  │  risk_score + mitre_techniques       │              │
//! │  │  written back to NormalizedEvent     │              │
//! │  │  + LocalAlert sent to transport      │              │
//! │  └─────────────────────────────────────┘              │
//! └──────────────────────────────────────────────────────┘
//! ```

