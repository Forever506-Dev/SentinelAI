// ─── Detection Rule Types ───────────────────────────────────────

/// A compiled Sigma rule ready for evaluation.
#[derive(Debug, Clone)]
pub struct CompiledSigmaRule {
    pub id: String,
    pub name: String,
    pub severity: Severity,
    pub mitre_techniques: Vec<String>,
    /// Pre-compiled filter expression tree.
    pub filter: FilterExpression,
    /// Event categories this rule applies to (skip evaluation for others).
    pub applicable_categories: Vec<EventCategory>,
}

/// Filter expression tree for efficient rule evaluation.
#[derive(Debug, Clone)]
pub enum FilterExpression {
    /// Field equals value (case-insensitive).
    FieldEquals { field: String, value: String },
    /// Field contains substring (case-insensitive).
    FieldContains { field: String, substring: String },
    /// Field matches regex pattern (pre-compiled).
    FieldRegex { field: String, pattern: String },
    /// Field starts with prefix.
    FieldStartsWith { field: String, prefix: String },
    /// Field ends with suffix.
    FieldEndsWith { field: String, suffix: String },
    /// Field exists and is non-empty.
    FieldExists { field: String },
    /// Field is in a set of values.
    FieldIn { field: String, values: Vec<String> },
    /// Logical AND of sub-expressions.
    And(Vec<FilterExpression>),
    /// Logical OR of sub-expressions.
    Or(Vec<FilterExpression>),
    /// Logical NOT of a sub-expression.
    Not(Box<FilterExpression>),
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord)]
pub enum Severity {
    Info = 0,
    Low = 1,
    Medium = 2,
    High = 3,
    Critical = 4,
}

/// Result of evaluating a single rule against an event.
#[derive(Debug, Clone)]
pub struct RuleMatch {
    pub rule_id: String,
    pub rule_name: String,
    pub severity: Severity,
    pub mitre_techniques: Vec<String>,
    pub confidence: f32, // 0.0 - 1.0
    pub context: HashMap<String, String>,
}

/// Local alert generated by the detection engine.
#[derive(Debug, Clone)]
pub struct LocalAlert {
    pub alert_id: String,
    pub rule_matches: Vec<RuleMatch>,
    pub risk_score: u32,
    pub triggering_event: NormalizedEvent,
    pub recommended_action: ResponseAction,
    pub detected_at_ns: u64,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ResponseAction {
    LogOnly,
    Alert,
    IsolateProcess,
    IsolateHost,
    CollectArtifact,
    KillProcess,
}

// ─── Detection Engine ───────────────────────────────────────────

/// The local detection engine that evaluates events against rules.
///
/// # Thread Safety
///
/// The engine is designed to be called from the event pipeline task.
/// Rule loading is done via interior mutability (RwLock on rules vec).
///
/// # Performance Target
///
/// - 500 Sigma rules evaluated in < 500μs per event
/// - YARA scan in < 10ms per file (only for file events with content)
/// - Total detection overhead: < 1ms per event (excluding YARA)
pub struct DetectionEngine {
    /// Compiled Sigma rules.
    sigma_rules: Vec<CompiledSigmaRule>,
    /// Built-in heuristic rules (hardcoded, always active).
    heuristic_rules: Vec<HeuristicRule>,
    /// YARA rule set (compiled, for file scanning).
    yara_rules: Option<YaraRuleSet>,
    /// IOC bloom filter for fast hash/domain/IP matching.
    ioc_filter: IocBloomFilter,
    /// Alert deduplication state.
    alert_dedup: AlertDedup,
    /// Metrics.
    metrics: DetectionMetrics,
}

